package ex2_control_statement;

public class Ex3_multiFor {
	public static void main(String[] args) {
		
		//*
		//* * 
		//* * *
		//* * * *
		//* * * * *
		
		for(int i = 1; i <= 5; i++) {
			
			for(int j = 0; j < i; j++) {
				
				System.out.print("* ");
				
			}//inner
			
			System.out.println();
		
		}//outer
		
	}//main

}
