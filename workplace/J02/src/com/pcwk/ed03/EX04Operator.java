package com.pcwk.ed03;

public class EX04Operator {

	public static void main(String[] args) {
		
		//비교연산자 : 양 변을 비교하여 같으면 참(true), 다르면 거짓(false)을 반환
		
		int a = 10;
		int b = 20;
		int c = 30;
		
		System.out.println("a<b : "+ (a<b));
		System.out.println("a>b : "+ (a>b));
		System.out.println(a+b<=c);
		System.out.println(a+b>=c);
		

	}

}
