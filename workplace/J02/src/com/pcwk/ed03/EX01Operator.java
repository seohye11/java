package com.pcwk.ed03;

public class EX01Operator {

	public static void main(String[] args) {
		
		// 산술연산자 : 변수와 상수와 함께 사용하여 기본적인 계산할 때 사용
		
		int a = 10; //= 대입 연산자
		int b = 3; //b에 3을 할당
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		

	}

}
