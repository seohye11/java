package com.pcwk.ed03;

public class EX03Oprerator {

	public static void main(String[] args) {
		
		//논리부정연산자 : 참(true)을 거짓으로 거짓(false)을 참으로 바꾸는 연산자
		
		boolean a = true;
		System.out.println("a = "+ a);
		System.out.println("!a = "+ !a);
		
		boolean b = false;
		System.out.println("b = "+ b);
		System.out.println("!b = "+ !b);

	}

}
